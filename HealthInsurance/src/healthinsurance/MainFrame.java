    //method to extract data from the form into the variables declared at the top
    public void extractFormData(){
        //TODO add your handling code here:
        
        firstName = txtFirstName.getText();
        surname = txtSurname.getText();
        gender = "";
        age = (String)comboAge.getSelectedItem();
        policyType = (String)comboPolicy.getSelectedItem();
        hospitalCover = (String)comboHospitalCover.getSelectedItem();
        healthConditions = listExistingHealthConditions.getSelectedValuesList();
        
        //getting the gender from the radio button's selection
        if (radioMale.isSelected()){
            gender = "Male";
        }
        else if (radioFemale.isSelected()){
            gender = "Female";
        }
        
        //printing out user data in the output window (shell) 
        System.out.println("\nName: " + firstName + " " + surname);
        System.out.println("Gender: " + gender);
        System.out.println("Age: " + age);
        System.out.println("Policy Type: " + policyType);
        System.out.println("Hospital Cover: " + hospitalCover);
        System.out.println("Health Condition(s): " + healthConditions);
        System.out.println("Total: " + finalquote);
        
        
        //printing true or false if all data is completely entered into the form
        boolean ok = validateForm();
        System.out.println(ok);
    }
    
    
    //method to assign what action is performed when the calculate button is clicked on the form 
    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        
        //calling the method to extract data from the form
        extractFormData();
        
        if (validateForm()){
            
            //creating a new Customer object
            customer = new Customer (firstName, surname, gender, age, healthConditions);
            
            
            //calculating a discount depending on the user's policy type 
            if (policyType.equals("Individual")){
                //creating a new Individul policy object
                iPolicy = new IndividualPolicy (hospitalCover);
                finalquote = iPolicy.calculate(customer);
            }
        
            else if (policyType.equals("Corporate")){
                //creating a new Corporate policy object
                cPolicy = new CorporatePolicy (hospitalCover);
                finalquote = cPolicy.calculate(customer);
            }
            
            //printing out the customer's final quote in the shell
            System.out.println("Final quote is: €" + finalquote);
            
            //formatting the customer's final quote into a string currency
            stringQuote = String.format("€%.2f", finalquote);
            txtTotal.setForeground(Color.green);
            
            //displaying the customer's final quote in a text field on the GUI form
            txtTotal.setText(stringQuote);
            
            //Enabling and disabling the clear form and calculate buttons respectively after the customer has calculated their quote
            btnClearForm.setEnabled(true);
            btnCalculate.setEnabled(false);
            
        }
        
    }//GEN-LAST:event_btnCalculateActionPerformed

    
    
