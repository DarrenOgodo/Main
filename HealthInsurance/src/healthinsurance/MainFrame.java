    //method to ensure that the user completes all necessary fields in the form 
    public boolean validateForm() {
        
        //declring the variable which will hold the error message.
        String error = "";
        
        //checking if the firstname and surname variables are empty 
        if (firstName.isEmpty()){
            error += "First Name\n";
        }
        if (surname.isEmpty()){
            error += "Surname\n";
        }
        
        //checking if both the male and female radio buttons are not selected 
        if (radioMale.isSelected() == false && radioFemale.isSelected() == false){
            error += "Gender\n";
        }
        
        //checking if the combo boxes are at their initial positions (index position[0])
        if (age.equals("Please Select")){
            error += "Age\n";
        }
        if (policyType.equals("Please Select")){
            error += "Policy Type\n";
        }
        if (hospitalCover.equals("Please Select")){
            error += "Hospital Cover\n";
        }
        
        
        //Displaying an error message saying "no quote provided" if the user's aged over 70 
        if (age.equals("Over 70")){
            JOptionPane.showMessageDialog(this,"No quote provided for people aged over 70");
            return false;
        }
        
        else if (error.equals("")){
            return true;
        }
        
        //displaying error message telling the user what fields were not completed
        else {
            JOptionPane.showMessageDialog(this,"The following fields were not completed:\n" + error);
            return false;
        }
    }
    
    
    
    
    
    
    
    
